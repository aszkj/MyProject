package com.yilidi.o2o.product.model;

import java.util.Date;

import com.yilidi.o2o.core.model.BaseModel;

/**
 * 功能描述：产品类别与店铺类型关系映射实体类，映射产品域表YiLiDiProductCenter.P_STORE_TYPE_PRODUCT_CLASS <br/>
 * 作者：zxs <br/>
 * 
 * BugID: <br/>
 * 修改内容： <br/>
 */
public class ProductClassStoreType extends BaseModel {

	private static final long serialVersionUID = -4246446818445901748L;
	/**
	 * 产品一级类别与产品基础类别映射实体ID，自增主键
	 */
	private Integer id;
	/**
	 * 店铺类型
	 */
	private String storeType;
	/**
	 * 产品一级类别编码
	 */
	private String classCode;
	/**
	 * 创建用户ID
	 */
	private Integer createUserId;
	/**
	 * 创建时间
	 */
	private Date createTime;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}


	public String getStoreType() {
		return storeType;
	}

	public void setStoreType(String storeType) {
		this.storeType = storeType;
	}

	public String getClassCode() {
		return classCode;
	}

	public void setClassCode(String classCode) {
		this.classCode = classCode;
	}

	public Integer getCreateUserId() {
		return createUserId;
	}

	public void setCreateUserId(Integer createUserId) {
		this.createUserId = createUserId;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

}