package com.yilidi.o2o.product.service.dto;

import com.yilidi.o2o.core.model.BaseDto;

/**
 * 功能描述：赠品奖券DTO <br/>
 * 作者：chenb <br/>
 * 
 * BugID: <br/>
 * 修改内容： <br/>
 */
public class RewardTicketDto extends BaseDto {

    private static final long serialVersionUID = 7331108771868308314L;

    /**
     * 奖券ID
     */
    private Integer ticketId;

    /**
     * 赠品奖券类型编码
     */
    private String ticketCode;
    /**
     * 奖券名称
     */
    private String ticketCodeName;
    /**
     * 金额
     */
    private Long ticketAmount;
    /**
     * 奖励数量
     */
    private Integer rewardNumber;

    /**
     * 奖券描述
     */
    private String ticketDescription;

    public String getTicketCode() {
        return ticketCode;
    }

    public void setTicketCode(String ticketCode) {
        this.ticketCode = ticketCode;
    }

    public String getTicketCodeName() {
        return ticketCodeName;
    }

    public void setTicketCodeName(String ticketCodeName) {
        this.ticketCodeName = ticketCodeName;
    }

    public String getTicketDescription() {
        return ticketDescription;
    }

    public void setTicketDescription(String ticketDescription) {
        this.ticketDescription = ticketDescription;
    }

    public Long getTicketAmount() {
        return ticketAmount;
    }

    public void setTicketAmount(Long ticketAmount) {
        this.ticketAmount = ticketAmount;
    }

    public Integer getTicketId() {
        return ticketId;
    }

    public void setTicketId(Integer ticketId) {
        this.ticketId = ticketId;
    }

    public Integer getRewardNumber() {
        return rewardNumber;
    }

    public void setRewardNumber(Integer rewardNumber) {
        this.rewardNumber = rewardNumber;
    }

}
