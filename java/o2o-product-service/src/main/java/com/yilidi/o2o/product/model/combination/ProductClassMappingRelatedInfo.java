package com.yilidi.o2o.product.model.combination;

import java.util.Date;

import com.yilidi.o2o.core.model.BaseModel;

/**
 * 功能描述：产品一级类别与产品基础类别映射实体信息Dto<br/>
 * 作者：zxs <br/>
 * 
 * BugID: <br/>
 * 修改内容： <br/>
 */
public class ProductClassMappingRelatedInfo extends BaseModel {
	private static final long serialVersionUID = 7693604464408421075L;
	/**
	 * 产品一级类别与产品基础类别映射实体ID，自增主键
	 */
	private Integer id;
	/**
	 * 产品类别编码
	 */
	private String classCode;
	/**
	 * 产品类别名称
	 */
	private String className;
	/**
	 * 店铺类别
	 */
	private String childClassCode;
	/**
	 * 店铺类别名称
	 */
	private String childClassName;
	/**
	 * 创建用户ID
	 */
	private Integer createUserId;
	/**
	 * 创建时间
	 */
	private Date createTime;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getClassCode() {
		return classCode;
	}

	public void setClassCode(String classCode) {
		this.classCode = classCode;
	}

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}

	public String getChildClassCode() {
		return childClassCode;
	}

	public void setChildClassCode(String childClassCode) {
		this.childClassCode = childClassCode;
	}

	public String getChildClassName() {
		return childClassName;
	}

	public void setChildClassName(String childClassName) {
		this.childClassName = childClassName;
	}

	public Integer getCreateUserId() {
		return createUserId;
	}

	public void setCreateUserId(Integer createUserId) {
		this.createUserId = createUserId;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

}