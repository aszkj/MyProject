package com.yilidi.o2o.product.model;

import java.util.Date;

import com.yilidi.o2o.core.model.BaseModel;

/**
 * 专题栏目信息实体类，映射产品域表YiLiDiProductCenter.P_THEME_COLUMN
 * 
 * @author: chenlian
 * @date: 2016年9月9日 下午3:20:27
 */
public class ThemeColumn extends BaseModel {

    private static final long serialVersionUID = -5205323864799407073L;

    /**
     * 专题栏目ID，自增主键
     */
    private Integer id;

    /**
     * 专题栏目名称
     */
    private String themeColumnName;

    /**
     * 专题栏目slogan图片URL
     */
    private String sloganImageUrl;

    /**
     * 排序
     */
    private Integer sort;

    /**
     * 专题栏目所关联的产品数量
     */
    private Integer quantity;

    /**
     * 专题栏目状态编码, 关联系统域系统字典S_STYSTEM_DICT表的DICTCODE字段(DICTTYPE=THEMECOLUMNSTATUS)
     */
    private String statusCode;

    /**
     * 专题ID，关联产品域P_THEME表的ID
     */
    private Integer themeId;

    /**
     * 创建用户ID
     */
    private Integer createUserId;

    /**
     * 创建时间
     */
    private Date createTime;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getThemeColumnName() {
        return themeColumnName;
    }

    public void setThemeColumnName(String themeColumnName) {
        this.themeColumnName = themeColumnName;
    }

    public String getSloganImageUrl() {
        return sloganImageUrl;
    }

    public void setSloganImageUrl(String sloganImageUrl) {
        this.sloganImageUrl = sloganImageUrl;
    }

    public Integer getSort() {
        return sort;
    }

    public void setSort(Integer sort) {
        this.sort = sort;
    }

    public Integer getQuantity() {
        return quantity;
    }

    public void setQuantity(Integer quantity) {
        this.quantity = quantity;
    }

    public String getStatusCode() {
        return statusCode;
    }

    public void setStatusCode(String statusCode) {
        this.statusCode = statusCode;
    }

    public Integer getThemeId() {
        return themeId;
    }

    public void setThemeId(Integer themeId) {
        this.themeId = themeId;
    }

    public Integer getCreateUserId() {
        return createUserId;
    }

    public void setCreateUserId(Integer createUserId) {
        this.createUserId = createUserId;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

}