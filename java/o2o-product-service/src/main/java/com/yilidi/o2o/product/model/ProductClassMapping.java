package com.yilidi.o2o.product.model;

import java.util.Date;

import com.yilidi.o2o.core.model.BaseModel;

/**
 * 功能描述：产品类别关系映射实体类，映射产品域表YiLiDiProductCenter.P_PRODUCT_CLASS_MAPPING <br/>
 * 作者：zxs <br/>
 * 
 * BugID: <br/>
 * 修改内容： <br/>
 */
public class ProductClassMapping extends BaseModel {

	private static final long serialVersionUID = 7455592627618826283L;
	/**
	 * 产品一级类别与产品基础类别映射实体ID，自增主键
	 */
	private Integer id;
	/**
	 * 产品一级类别编码
	 */
	private String classCode;
	/**
	 * 产品基础类别编码
	 */
	private String childClassCode;
	/**
	 * 创建用户ID
	 */
	private Integer createUserId;
	/**
	 * 创建时间
	 */
	private Date createTime;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getClassCode() {
		return classCode;
	}

	public void setClassCode(String classCode) {
		this.classCode = classCode;
	}

	public String getChildClassCode() {
		return childClassCode;
	}

	public void setChildClassCode(String childClassCode) {
		this.childClassCode = childClassCode;
	}

	public Integer getCreateUserId() {
		return createUserId;
	}

	public void setCreateUserId(Integer createUserId) {
		this.createUserId = createUserId;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

}