package com.yilidi.o2o.product.model;

import java.util.Date;

import com.yilidi.o2o.core.model.BaseModel;

/**
 * 功能描述：产品类别实体类，映射产品域表YiLiDiProductCenter.P_PRODUCT_CLASS <br/>
 * 作者：chenl <br/>
 * 
 * BugID: <br/>
 * 修改内容： <br/>
 */
public class ProductClass extends BaseModel {
    private static final long serialVersionUID = 5902967538485353397L;
    private static final String DEFAULT_STATUSCODE = "PRODUCTCLASSSTATUS_ON";

    public ProductClass() {
        this.statusCode = DEFAULT_STATUSCODE;
    }

    /**
     * 产品类别ID，自增主键
     */
    private Integer id;
    /**
     * 产品类别编码
     */
    private String classCode;
    /**
     * 产品类别级别，关联系统域系统字典S_SYSTEM_DICT中的DICTCODE字段(DICTTYPE=PRODUCTCLASSLEVEL)
     */
    private String classLevel;
    /**
     * 产品类别url
     */
    private String classImageUrl;
    /**
     * 产品类别排序
     */
    private Integer classSort;
    /**
     * 产品类别名称
     */
    private String className;
    /**
     * 状态编码，关联系统域系统字典S_SYSTEM_DICT中的DICTCODE字段(DICTTYPE=PRODUCTCLASSSTATUS)
     */
    private String statusCode;
    /**
     * 是否显示，关联系统域系统字典S_SYSTEM_DICT中的DICTCODE字段(DICTTYPE=PRODUCTCLASSDISPLAY)
     */
    private String display;
    /**
     * 产品类别描述
     */
    private String note;
    /**
     * 创建用户ID
     */
    private Integer createUserId;
    /**
     * 创建时间
     */
    private Date createTime;
    /**
     * 修改用户ID
     */
    private Integer modifyUserId;
    /**
     * 修改时间
     */
    private Date modifyTime;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getClassCode() {
        return classCode;
    }

    public void setClassCode(String classCode) {
        this.classCode = classCode;
    }

    public String getClassLevel() {
        return classLevel;
    }

    public void setClassLevel(String classLevel) {
        this.classLevel = classLevel;
    }

    public String getClassImageUrl() {
        return classImageUrl;
    }

    public void setClassImageUrl(String classImageUrl) {
        this.classImageUrl = classImageUrl;
    }

    public Integer getClassSort() {
        return classSort;
    }

    public void setClassSort(Integer classSort) {
        this.classSort = classSort;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getStatusCode() {
        return statusCode;
    }

    public void setStatusCode(String statusCode) {
        this.statusCode = statusCode;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public Integer getCreateUserId() {
        return createUserId;
    }

    public void setCreateUserId(Integer createUserId) {
        this.createUserId = createUserId;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Integer getModifyUserId() {
        return modifyUserId;
    }

    public void setModifyUserId(Integer modifyUserId) {
        this.modifyUserId = modifyUserId;
    }

    public Date getModifyTime() {
        return modifyTime;
    }

    public void setModifyTime(Date modifyTime) {
        this.modifyTime = modifyTime;
    }

    public String getDisplay() {
        return display;
    }

    public void setDisplay(String display) {
        this.display = display;
    }

}