package com.yilidi.o2o.product.model;

import com.yilidi.o2o.core.model.BaseModel;

/**
 * 功能描述：产品基本属性值历史实体类，映射产品域表YiLiDiProductCenter.P_PRODUCT_BASIC_PROPS_VALUE_HISTORY <br/>
 * 作者：zxs <br/>
 * 
 * BugID: <br/>
 * 修改内容： <br/>
 */
public class ProductBasicPropsValueHistory extends BaseModel {
	private static final long serialVersionUID = 4239977494503237501L;
	/**
	 * 属性值历史ID，自增主键
	 */
	private Integer id;
	/**
	 * 属性值ID，关联产品类型定义表P_PRODUCT_BASIC_PROPS_VALUE的Id字段
	 */
	private Integer productBasicPropsValueId;
	/**
	 * 产品类别编码，关联产品类型定义表P_PRODUCT_CLASS的PRODUCTCLASSCODE字段
	 */
	private String productClassCode;
	/**
	 * 产品ID, 关联产品域产品表的PRODUCTID字段
	 */
	private Integer productId;
	/**
	 * 属性名称,该属性值修改前的属性名称
	 */
	private String oriPropsName;
	/**
	 * 属性名称， 该属性值修改后的属性名称
	 */
	private String propsName;
	/**
	 * 属性编码，关联产品扩展属性表P_PRODUCT_BASIC_PROPS的属性编码PROPSCODE字段
	 */
	private String propsCode;
	/**
	 * 属性取值，该属性值修改前的取值内容
	 */
	private String oriPropsValue;
	/**
	 * 属性取值，该属性修改后的取值内容
	 */
	private String propsValue;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getProductBasicPropsValueId() {
		return productBasicPropsValueId;
	}

	public void setProductBasicPropsValueId(Integer productBasicPropsValueId) {
		this.productBasicPropsValueId = productBasicPropsValueId;
	}

	public String getProductClassCode() {
		return productClassCode;
	}

	public void setProductClassCode(String productClassCode) {
		this.productClassCode = productClassCode;
	}

	public Integer getProductId() {
		return productId;
	}

	public void setProductId(Integer productId) {
		this.productId = productId;
	}

	public String getOriPropsName() {
		return oriPropsName;
	}

	public void setOriPropsName(String oriPropsName) {
		this.oriPropsName = oriPropsName;
	}

	public String getPropsName() {
		return propsName;
	}

	public void setPropsName(String propsName) {
		this.propsName = propsName;
	}

	public String getPropsCode() {
		return propsCode;
	}

	public void setPropsCode(String propsCode) {
		this.propsCode = propsCode;
	}

	public String getOriPropsValue() {
		return oriPropsValue;
	}

	public void setOriPropsValue(String oriPropsValue) {
		this.oriPropsValue = oriPropsValue;
	}

	public String getPropsValue() {
		return propsValue;
	}

	public void setPropsValue(String propsValue) {
		this.propsValue = propsValue;
	}

}