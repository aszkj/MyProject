package com.yilidi.o2o.product.service.dto;

import java.util.Date;

import com.yilidi.o2o.core.model.BaseDto;

/**
 * 功能描述：商品图片模型Dto <br/>
 * 作者：zxs <br/>
 * 
 * BugID: <br/>
 * 修改内容： <br/>
 */
public class SaleProductImageDto extends BaseDto {
	private static final long serialVersionUID = 4162049523878331278L;

	private static final Integer DEFAULT_IMAGEORDER = 1;

	public SaleProductImageDto() {
		this.imageOrder = DEFAULT_IMAGEORDER;
	}

	/**
	 * 图片ID， 主键自增
	 */
	private Integer id;
	/**
	 * 店铺商品ID
	 */
	private Integer saleProductId;
	/**
	 * 渠道编码
	 */
	private String channelCode;
	/**
	 * 主图标志
	 */
	private String masterFlag;
	/**
	 * 图片URL参数
	 */
	private String imageUrl;
	/**
	 * 返回图片URL1
	 */
	private String imageUrl1;
	/**
	 * 返回图片URL2
	 */
	private String imageUrl2;
	/**
	 * 返回图片URL3
	 */
	private String imageUrl3;
	/**
	 * 图片顺序，图片显示顺序（编号小的先显示，如果编号相同，则id小的先显示）
	 */
	private Integer imageOrder;
	/**
	 * 创建时间
	 */
	private Date createTime;
	/**
	 * 创建用户ID
	 */
	private Integer createUserId;
	/**
	 * 修改时间
	 */
	private Date modifyTime;
	/**
	 * 修改用户ID
	 */
	private Integer modifyUserId;
	/**
	 * APP产品图片增加标示
	 */
	private String appImageFlag;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getSaleProductId() {
		return saleProductId;
	}

	public void setSaleProductId(Integer saleProductId) {
		this.saleProductId = saleProductId;
	}

	public String getChannelCode() {
		return channelCode;
	}

	public void setChannelCode(String channelCode) {
		this.channelCode = channelCode;
	}

	public String getMasterFlag() {
		return masterFlag;
	}

	public void setMasterFlag(String masterFlag) {
		this.masterFlag = masterFlag;
	}

	public String getImageUrl() {
		return imageUrl;
	}

	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}

	public String getImageUrl1() {
		return imageUrl1;
	}

	public void setImageUrl1(String imageUrl1) {
		this.imageUrl1 = imageUrl1;
	}

	public String getImageUrl2() {
		return imageUrl2;
	}

	public void setImageUrl2(String imageUrl2) {
		this.imageUrl2 = imageUrl2;
	}

	public String getImageUrl3() {
		return imageUrl3;
	}

	public void setImageUrl3(String imageUrl3) {
		this.imageUrl3 = imageUrl3;
	}

	public Integer getImageOrder() {
		return imageOrder;
	}

	public void setImageOrder(Integer imageOrder) {
		this.imageOrder = imageOrder;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Integer getCreateUserId() {
		return createUserId;
	}

	public void setCreateUserId(Integer createUserId) {
		this.createUserId = createUserId;
	}

	public Date getModifyTime() {
		return modifyTime;
	}

	public void setModifyTime(Date modifyTime) {
		this.modifyTime = modifyTime;
	}

	public Integer getModifyUserId() {
		return modifyUserId;
	}

	public void setModifyUserId(Integer modifyUserId) {
		this.modifyUserId = modifyUserId;
	}

	public String getAppImageFlag() {
		return appImageFlag;
	}

	public void setAppImageFlag(String appImageFlag) {
		this.appImageFlag = appImageFlag;
	}

}