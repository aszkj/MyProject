package com.yilidi.o2o.appvo.buyer.user;

import com.yilidi.o2o.appvo.AppBaseVO;

/**
 * 新增/修改地址信息接口
 * 
 * @author: chenb
 * @date: 2016年6月21日 下午6:49:30
 */
public class AddressSaveOrUpdateVO extends AppBaseVO {

    private static final long serialVersionUID = 4971955272559408454L;
    /** 收货人姓名 **/
    private Integer addressId;

    /** 收货人姓名 **/
    private String consigneeName;

    /** 手机号 **/
    private String phoneNo;

    /** 详细地址信息 **/
    private String addressDetail;

    /** 小区信息 **/
    private CommunitySearchVO community;

    public Integer getAddressId() {
        return addressId;
    }

    public void setAddressId(Integer addressId) {
        this.addressId = addressId;
    }

    public String getConsigneeName() {
        return consigneeName;
    }

    public void setConsigneeName(String consigneeName) {
        this.consigneeName = consigneeName;
    }

    public String getPhoneNo() {
        return phoneNo;
    }

    public void setPhoneNo(String phoneNo) {
        this.phoneNo = phoneNo;
    }

    public String getAddressDetail() {
        return addressDetail;
    }

    public void setAddressDetail(String addressDetail) {
        this.addressDetail = addressDetail;
    }

    public CommunitySearchVO getCommunity() {
        return community;
    }

    public void setCommunity(CommunitySearchVO community) {
        this.community = community;
    }

}
