package com.yilidi.o2o.appvo.buyer.user;

import com.yilidi.o2o.appvo.AppBaseVO;

/**
 * 普通账本
 * 
 * @author: chenb
 * @date: 2016年5月27日 上午11:41:15
 */
public class CommonAccountVO extends AppBaseVO {

    private static final long serialVersionUID = -4894417282718562561L;

    /**
     * 账本名称
     */
    private String accountName;
    /**
     * 账本数值
     */
    private Long accountNum;
    /**
     * 账本数值类型：<br>
     * 1：RMB金额（单位为分，即展示效果应该除以100进行金额展示）</br>
     * 2：自然数
     */
    private Integer accountNumType;

    public String getAccountName() {
        return accountName;
    }

    public void setAccountName(String accountName) {
        this.accountName = accountName;
    }

    public Long getAccountNum() {
        return accountNum;
    }

    public void setAccountNum(Long accountNum) {
        this.accountNum = accountNum;
    }

    public Integer getAccountNumType() {
        return accountNumType;
    }

    public void setAccountNumType(Integer accountNumType) {
        this.accountNumType = accountNumType;
    }

}
