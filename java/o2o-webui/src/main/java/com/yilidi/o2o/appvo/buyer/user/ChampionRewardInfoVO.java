package com.yilidi.o2o.appvo.buyer.user;

import com.yilidi.o2o.appvo.AppBaseVO;

/**
 * 分享好礼冠军信息
 * 
 * @author: chenb
 * @date: 2016年5月27日 上午11:41:15
 */
public class ChampionRewardInfoVO extends AppBaseVO {

    private static final long serialVersionUID = -1068139148749696658L;
    /**
     * 在什么时间内获得的冠军
     */
    private String period;
    /**
     * 冠军名称
     */
    private String championName;
    /**
     * 奖励名称
     */
    private String rewardName;

    public String getPeriod() {
        return period;
    }

    public void setPeriod(String period) {
        this.period = period;
    }

    public String getChampionName() {
        return championName;
    }

    public void setChampionName(String championName) {
        this.championName = championName;
    }

    public String getRewardName() {
        return rewardName;
    }

    public void setRewardName(String rewardName) {
        this.rewardName = rewardName;
    }

}
