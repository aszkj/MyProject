package com.yilidi.o2o.appvo.buyer.system;

import java.util.Date;

import com.yilidi.o2o.appvo.AppBaseVO;

public class SystemMessageVO extends AppBaseVO{
	private static final long serialVersionUID = 1L;
	private Integer msgId; //用户消息id
	private String msgTitle;//用户消息标题
	private String msgImage;//用户消息图片内容URL
	private Date msgTime;//消息发生时间
	private String msgAbstract;//消息内容摘要
	private Integer directType;//跳转类型
	private String directCode;//跳转内容
	private String msgContent;
	public Integer getMsgId() {
		return msgId;
	}
	public void setMsgId(Integer msgId) {
		this.msgId = msgId;
	}
	public String getMsgTitle() {
		return msgTitle;
	}
	public void setMsgTitle(String msgTitle) {
		this.msgTitle = msgTitle;
	}
	public String getMsgImage() {
		return msgImage;
	}
	public void setMsgImage(String msgImage) {
		this.msgImage = msgImage;
	}
	public Date getMsgTime() {
		return msgTime;
	}
	public void setMsgTime(Date msgTime) {
		this.msgTime = msgTime;
	}
	public String getMsgAbstract() {
		return msgAbstract;
	}
	public void setMsgAbstract(String msgAbstract) {
		this.msgAbstract = msgAbstract;
	}
	public Integer getDirectType() {
		return directType;
	}
	public void setDirectType(Integer directType) {
		this.directType = directType;
	}
	public String getDirectCode() {
		return directCode;
	}
	public void setDirectCode(String directCode) {
		this.directCode = directCode;
	}
	public String getMsgContent() {
		return msgContent;
	}
	public void setMsgContent(String msgContent) {
		this.msgContent = msgContent;
	}
}
