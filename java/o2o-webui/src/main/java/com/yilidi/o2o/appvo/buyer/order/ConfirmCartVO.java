package com.yilidi.o2o.appvo.buyer.order;

import java.util.List;

import com.yilidi.o2o.appvo.AppBaseVO;

/**
 * 确认购物车
 * 
 * @author: chenb
 * @date: 2016年5月27日 下午7:26:46
 */
public class ConfirmCartVO extends AppBaseVO {
    private static final long serialVersionUID = 4971955272559408454L;

    /**
     * 系统当前时间
     */
    private String currentTime;
    /**
     * 收货地址
     */
    private ConsigneeAddressBeanVO consigneeAddress;

    /**
     * 购物车列表
     */
    private List<ShopCartListVO> shopCartList;

    public String getCurrentTime() {
        return currentTime;
    }

    public void setCurrentTime(String currentTime) {
        this.currentTime = currentTime;
    }

    public ConsigneeAddressBeanVO getConsigneeAddress() {
        return consigneeAddress;
    }

    public void setConsigneeAddress(ConsigneeAddressBeanVO consigneeAddress) {
        this.consigneeAddress = consigneeAddress;
    }

    public List<ShopCartListVO> getShopCartList() {
        return shopCartList;
    }

    public void setShopCartList(List<ShopCartListVO> shopCartList) {
        this.shopCartList = shopCartList;
    }

}
