package com.yilidi.o2o.appvo.buyer.product;

import java.util.List;

import com.yilidi.o2o.appvo.AppBaseVO;

/**
 * 查询专题信息数据接口出参实体
 * 
 * @author: chenb
 * @date: 2016年5月30日 下午9:03:43
 */
public class SeminarInfoVO extends AppBaseVO {
    private static final long serialVersionUID = -5384790954740097792L;

    /**
     * 专题code
     */
    private String seminarCode;
    /**
     * 专题名称
     */
    private String seminarName;
    /**
     * 专题内图片URL
     */
    private String imageUrl;
    /**
     * 专题slogan图片URL
     */
    private String sloganImageUrl;
    /**
     * 专题背景颜色
     */
    private String backgroundColor;
    /**
     * 专题商品列表
     */
    private List<SaleProductBaseVO> saleProducts;

    public String getSeminarCode() {
        return seminarCode;
    }

    public void setSeminarCode(String seminarCode) {
        this.seminarCode = seminarCode;
    }

    public String getSeminarName() {
        return seminarName;
    }

    public void setSeminarName(String seminarName) {
        this.seminarName = seminarName;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getSloganImageUrl() {
        return sloganImageUrl;
    }

    public void setSloganImageUrl(String sloganImageUrl) {
        this.sloganImageUrl = sloganImageUrl;
    }

    public String getBackgroundColor() {
        return backgroundColor;
    }

    public void setBackgroundColor(String backgroundColor) {
        this.backgroundColor = backgroundColor;
    }

    public List<SaleProductBaseVO> getSaleProducts() {
        return saleProducts;
    }

    public void setSaleProducts(List<SaleProductBaseVO> saleProducts) {
        this.saleProducts = saleProducts;
    }

}
