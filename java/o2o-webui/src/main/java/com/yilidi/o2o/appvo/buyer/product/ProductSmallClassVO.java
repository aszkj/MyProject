package com.yilidi.o2o.appvo.buyer.product;

import com.yilidi.o2o.appvo.AppBaseVO;

/**
 * 商品小类
 * 
 * @author: chenb
 * @date: 2016年5月27日 下午7:26:46
 */
public class ProductSmallClassVO extends AppBaseVO {
    private static final long serialVersionUID = 4971955272559408454L;

    /** 商品分类CODE **/
    private String classCode;
    /** 商品分类名称 **/
    private String className;

    public ProductSmallClassVO() {
    }

    public ProductSmallClassVO(String classCode, String className) {
        this.classCode = classCode;
        this.className = className;
    }

    public String getClassCode() {
        return classCode;
    }

    public void setClassCode(String classCode) {
        this.classCode = classCode;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

}
