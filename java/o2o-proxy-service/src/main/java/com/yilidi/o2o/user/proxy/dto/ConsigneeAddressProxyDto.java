package com.yilidi.o2o.user.proxy.dto;

import com.yilidi.o2o.core.model.BaseDto;

/**
 * 功能描述：收货地址信息 <br/>
 * 作者：chenl <br/>
 * 
 * BugID: <br/>
 * 修改内容： <br/>
 */
public class ConsigneeAddressProxyDto extends BaseDto {
    private static final long serialVersionUID = 434455647322939097L;

    /**
     * 地址id
     */
    private Integer id;
    /**
     * 客户ID
     */
    private Integer customerId;
    /**
     * 省份编码
     */
    private String provinceCode;
    /**
     * 市编码，
     */
    private String cityCode;
    /**
     * 区/县编码
     */
    private String countyCode;
    /**
     * 乡/镇编码， 关联系统域表S_AREADICT的AREACODE字段
     */
    private String townCode;
    /**
     * 具体地址信息
     */
    private String addressDetail;

    /**
     * 收货人姓名
     */
    private String consigneeName;
    /**
     * 收货人手机号码
     */
    private String phoneNo;

    /**
     * 状态, "CONSADDRSTATUS_OFF"禁用 ，"CONSADDRSTATUS_ON" 启用
     */
    private String status;
    /**
     * 小区ID
     */
    private Integer communityId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getCustomerId() {
        return customerId;
    }

    public void setCustomerId(Integer customerId) {
        this.customerId = customerId;
    }

    public String getProvinceCode() {
        return provinceCode;
    }

    public void setProvinceCode(String provinceCode) {
        this.provinceCode = provinceCode;
    }

    public String getCityCode() {
        return cityCode;
    }

    public void setCityCode(String cityCode) {
        this.cityCode = cityCode;
    }

    public String getCountyCode() {
        return countyCode;
    }

    public void setCountyCode(String countyCode) {
        this.countyCode = countyCode;
    }

    public String getTownCode() {
        return townCode;
    }

    public void setTownCode(String townCode) {
        this.townCode = townCode;
    }

    public String getAddressDetail() {
        return addressDetail;
    }

    public void setAddressDetail(String addressDetail) {
        this.addressDetail = addressDetail;
    }

    public String getConsigneeName() {
        return consigneeName;
    }

    public void setConsigneeName(String consigneeName) {
        this.consigneeName = consigneeName;
    }

    public String getPhoneNo() {
        return phoneNo;
    }

    public void setPhoneNo(String phoneNo) {
        this.phoneNo = phoneNo;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Integer getCommunityId() {
        return communityId;
    }

    public void setCommunityId(Integer communityId) {
        this.communityId = communityId;
    }

}
