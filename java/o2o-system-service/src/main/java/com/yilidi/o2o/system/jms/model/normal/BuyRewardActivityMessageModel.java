package com.yilidi.o2o.system.jms.model.normal;

import java.util.Date;

import com.yilidi.o2o.core.model.JmsMessageModel;

public class BuyRewardActivityMessageModel extends JmsMessageModel {

    private static final long serialVersionUID = -7121317037737606144L;
    /**
     * 下单号
     */
    private String orderNo;
    /**
     * 奖励类型
     */
    private String giftType;
    /**
     * 活动类型,扩展字段
     */
    private String activityType;
    /**
     * 当前时间
     */
    private Date nowTime;
    /**
     * 操作用户ID
     */
    private Integer operationUserId;

    public String getOrderNo() {
        return orderNo;
    }

    public void setOrderNo(String orderNo) {
        this.orderNo = orderNo;
    }

    public Date getNowTime() {
        return nowTime;
    }

    public void setNowTime(Date nowTime) {
        this.nowTime = nowTime;
    }

    public String getGiftType() {
        return giftType;
    }

    public void setGiftType(String giftType) {
        this.giftType = giftType;
    }

    public String getActivityType() {
        return activityType;
    }

    public void setActivityType(String activityType) {
        this.activityType = activityType;
    }

    public Integer getOperationUserId() {
        return operationUserId;
    }

    public void setOperationUserId(Integer operationUserId) {
        this.operationUserId = operationUserId;
    }

}
