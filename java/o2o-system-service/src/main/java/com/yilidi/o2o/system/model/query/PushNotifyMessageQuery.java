package com.yilidi.o2o.system.model.query;

import java.util.Date;

import com.yilidi.o2o.core.model.BaseQuery;

/**
 * 推送通知查询实体类
 * 
 * @author: chenlian
 * @date: 2016年8月8日 下午2:01:32
 */
public class PushNotifyMessageQuery extends BaseQuery {

    /**
     * serialVersionUID
     */
    private static final long serialVersionUID = 1030647889214580514L;

    /**
     * 接受推送通知的用户ID
     */
    private Integer toUser;

    /**
     * 发送开始时间
     */
    private String startSendTime;

    /**
     * 发送结束时间
     */
    private String endSendTime;

    /**
     * 发送开始时间
     */
    private Date startSendDate;

    /**
     * 发送结束时间
     */
    private Date endSendDate;

    /**
     * 消息发送状态，关联系统域系统字典S_SYSTEM_DICT中的DICTCODE字段(DICTTYPE=NOTIFYMSGSTATUS)
     */
    private String messageStatus;

    /**
     * 推送消息类型，关联系统域系统字典S_SYSTEM_DICT中的DICTCODE字段(DICTTYPE=PUSHNOTIFYMSGTYPE)
     */
    private String pushMsgType;

    /**
     * 推送消息提供方类型，关联系统域系统字典S_SYSTEM_DICT中的DICTCODE字段(DICTTYPE=PUSHNOTIFYPROVIDETYPE)
     */
    private String pushProvideType;

    /**
     * 消息平台的JOBID
     */
    private String pushJobId;

    public Integer getToUser() {
        return toUser;
    }

    public void setToUser(Integer toUser) {
        this.toUser = toUser;
    }

    public String getStartSendTime() {
        return startSendTime;
    }

    public void setStartSendTime(String startSendTime) {
        this.startSendTime = startSendTime;
    }

    public String getEndSendTime() {
        return endSendTime;
    }

    public void setEndSendTime(String endSendTime) {
        this.endSendTime = endSendTime;
    }

    public Date getStartSendDate() {
        return startSendDate;
    }

    public void setStartSendDate(Date startSendDate) {
        this.startSendDate = startSendDate;
    }

    public Date getEndSendDate() {
        return endSendDate;
    }

    public void setEndSendDate(Date endSendDate) {
        this.endSendDate = endSendDate;
    }

    public String getMessageStatus() {
        return messageStatus;
    }

    public void setMessageStatus(String messageStatus) {
        this.messageStatus = messageStatus;
    }

    public String getPushMsgType() {
        return pushMsgType;
    }

    public void setPushMsgType(String pushMsgType) {
        this.pushMsgType = pushMsgType;
    }

    public String getPushProvideType() {
        return pushProvideType;
    }

    public void setPushProvideType(String pushProvideType) {
        this.pushProvideType = pushProvideType;
    }

    public String getPushJobId() {
        return pushJobId;
    }

    public void setPushJobId(String pushJobId) {
        this.pushJobId = pushJobId;
    }

}
