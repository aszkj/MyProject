package com.yilidi.o2o.core.exception;

public class ReportException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1105307651898331030L;

	public ReportException() {
		super();
	}

	public ReportException(String arg0) {
		super(arg0);
	}

	public ReportException(Throwable arg1) {
		super(arg1);
	}

	public ReportException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
