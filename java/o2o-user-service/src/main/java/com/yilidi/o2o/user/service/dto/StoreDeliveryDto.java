package com.yilidi.o2o.user.service.dto;

import java.util.Date;

import com.yilidi.o2o.core.model.BaseDto;

/**
 * 店铺接单员DTO
 * 
 * @author: heyong
 * @date: 2015年12月10日 上午11:25:19
 * 
 */
public class StoreDeliveryDto extends BaseDto {

	private static final long serialVersionUID = 6122647036154336913L;

	/**
	 * ID，自增主键
	 */
	private Integer id;

	/**
	 * 店铺ID, 关联用户域U_STORE_PROFILE表的ID
	 */
	private Integer storeId;

	/**
	 * 接单员登录账号： （对应user表的username）
	 */
	private String userName;

	/**
	 * 联系人
	 */
	private String contact;

	/**
	 * 手机号码
	 */
	private String mobile;

	/**
	 * 是否有效： （对应系统域系统字典S_SYETEM_DICT表的DICTTYPE=STOREDELIVERYSTATE的内容）
	 */
	private String state;

	/**
	 * 备注
	 */
	private String remark;

	/**
	 * 门店编号
	 */
	private String storeCode;

	/**
	 * 门店名称
	 */
	private String storeName;

	/**
	 * 创建人ID
	 */
	private Integer createUserId;

	/**
	 * 创建时间
	 */
	private Date createDate;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getStoreId() {
		return storeId;
	}

	public void setStoreId(Integer storeId) {
		this.storeId = storeId;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getContact() {
		return contact;
	}

	public void setContact(String contact) {
		this.contact = contact;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getStoreCode() {
		return storeCode;
	}

	public void setStoreCode(String storeCode) {
		this.storeCode = storeCode;
	}

	public String getStoreName() {
		return storeName;
	}

	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}

	public Integer getCreateUserId() {
		return createUserId;
	}

	public void setCreateUserId(Integer createUserId) {
		this.createUserId = createUserId;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

}
