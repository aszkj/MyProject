package com.yilidi.o2o.user.service.dto.query;

import com.yilidi.o2o.core.model.BaseQuery;

/**
 * 账户绑定银行卡，支付宝，微信支付等管理查询条件封装对象
 * 
 * @Description:TODO(账户绑定银行卡，支付宝，微信支付等管理查询条件封装对象) 
 * @author:	llp
 * @date:	2015年11月5日 下午3:26:59 
 *
 */
public class AccountBindingQuery extends BaseQuery {

	private static final long serialVersionUID = -1218645834967289425L;
	/**
	 * 地市编码
	 */
	private String cityCode;
	/**
	 * 区县编码
	 */
	private String countyCode;
	/**
	 * 客户名称：零售商名称，供应商名称，运营商（机构）名称
	 */
	private String customerName;
	/**
	 * 门店编码
	 */
	private String storeCode;
	/**
	 * 绑定银行卡用户名称
	 */
	private String masterName;
	/**
	 * 用户联系电话
	 */
	private String telPhone;

	/**
	 * 开户银行，开户类型：支付宝，微信支付
	 */
	private String accountBindingType;
	
	public String getCityCode() {
		return cityCode;
	}
	public void setCityCode(String cityCode) {
		this.cityCode = cityCode;
	}
	public String getCountyCode() {
		return countyCode;
	}
	public void setCountryCode(String countyCode) {
		this.countyCode = countyCode;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getMasterName() {
		return masterName;
	}
	public void setMasterName(String masterName) {
		this.masterName = masterName;
	}
	public String getTelPhone() {
		return telPhone;
	}
	public void setTelPhone(String telPhone) {
		this.telPhone = telPhone;
	}
	public String getStoreCode() {
		return storeCode;
	}
	public void setStoreCode(String storeCode) {
		this.storeCode = storeCode;
	}
	public String getAccountBindingType() {
		return accountBindingType;
	}
	public void setAccountBindingType(String accountBindingType) {
		this.accountBindingType = accountBindingType;
	}
}
