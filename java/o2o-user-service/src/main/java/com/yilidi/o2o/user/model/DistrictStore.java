package com.yilidi.o2o.user.model;

import com.yilidi.o2o.core.model.BaseModel;

/**
 * 区域店铺关联实体类，映射数据库表 YiLiDiUserCenter.U_DISTRICT_STORE
 * 
 * @author: heyong
 * @date: 2015年11月11日 上午11:54:37
 * 
 */
public class DistrictStore extends BaseModel {

	private static final long serialVersionUID = 9122517085982612926L;

	/**
	 * ID，自增主键
	 */
	private Integer id;

	/**
	 * 小区ID, 关联用户域U_COMMUNITY表的ID
	 */
	private Integer communityId;
	/**
	 * 店铺ID, 关联用户域U_STORE_PROFILE表的STOREID
	 */
	private Integer storeId;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getCommunityId() {
		return communityId;
	}

	public void setCommunityId(Integer communityId) {
		this.communityId = communityId;
	}

	public Integer getStoreId() {
		return storeId;
	}

	public void setStoreId(Integer storeId) {
		this.storeId = storeId;
	}

}
