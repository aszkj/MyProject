package com.yilidi.o2o.order.model.query;

import java.util.Date;

import com.yilidi.o2o.core.model.BaseQuery;

/**
 * 支付日志查询数据层类
 * 
 * @author simpson
 * 
 */
public class PayLogQuery extends BaseQuery {

    private static final long serialVersionUID = -483830276527664660L;
    /**
     * 支付日志编号
     */
    private String payLogOrderNo;
    /**
     * 支付平台流水号
     */
    private String paySequence;
    /**
     * 支付日志类型，关联系统域系统字典S_SYSTEM_DICT中的DICTCODE字段(DICTTYPE=PAYLOGTYPE)
     */
    private Integer payLogType;
    /**
     * 支付用户名称
     */
    private String userName;
    /**
     * 订单编号
     */
    private String saleOrderNo;
    /**
     * 支付平台编码，关联系统域系统字典S_SYSTEM_DICT中的DICTCODE字段(DICTTYPE=SALEORDERPAYPLATFORM)
     */
    private String payPlatformCode;
    /**
     * 支付日志状态，关联系统域系统字典S_SYSTEM_DICT中的DICTCODE字段(DICTTYPE=PAYLOGSTATUS)
     */
    private String payLogStatus;
    /**
     * 创建开始时间
     */
    private Date beginCreateTime;
    /**
     * 创建结束时间
     */
    private Date endCreateTime;
    /**
     * 更新开始时间
     */
    private Date beginUpdateTime;
    /**
     * 更新结束时间
     */
    private Date endUpdateTime;
    /**
     * 更新开始时间字符串
     */
    private String strBeginUpdateTime;

    /**
     * 更新结束时间字符串
     */
    private String strEndUpdateTime;

    /**
     * 开始时间字符串
     */
    private String strBeginCreateTime;

    /**
     * 结束时间字符串
     */
    private String strEndCreateTime;

    
    public String getPaySequence() {
		return paySequence;
	}

	public void setPaySequence(String paySequence) {
		this.paySequence = paySequence;
	}

	public String getStrBeginUpdateTime() {
		return strBeginUpdateTime;
	}

	public void setStrBeginUpdateTime(String strBeginUpdateTime) {
		this.strBeginUpdateTime = strBeginUpdateTime;
	}

	public String getStrEndUpdateTime() {
		return strEndUpdateTime;
	}

	public void setStrEndUpdateTime(String strEndUpdateTime) {
		this.strEndUpdateTime = strEndUpdateTime;
	}

	public String getStrBeginCreateTime() {
		return strBeginCreateTime;
	}

	public void setStrBeginCreateTime(String strBeginCreateTime) {
		this.strBeginCreateTime = strBeginCreateTime;
	}

	public String getStrEndCreateTime() {
		return strEndCreateTime;
	}

	public void setStrEndCreateTime(String strEndCreateTime) {
		this.strEndCreateTime = strEndCreateTime;
	}

	public String getPayLogOrderNo() {
        return payLogOrderNo;
    }

    public void setPayLogOrderNo(String payLogOrderNo) {
        this.payLogOrderNo = payLogOrderNo;
    }

    public Integer getPayLogType() {
        return payLogType;
    }

    public void setPayLogType(Integer payLogType) {
        this.payLogType = payLogType;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getSaleOrderNo() {
        return saleOrderNo;
    }

    public void setSaleOrderNo(String saleOrderNo) {
        this.saleOrderNo = saleOrderNo;
    }

    public String getPayPlatformCode() {
        return payPlatformCode;
    }

    public void setPayPlatformCode(String payPlatformCode) {
        this.payPlatformCode = payPlatformCode;
    }

    public String getPayLogStatus() {
        return payLogStatus;
    }

    public void setPayLogStatus(String payLogStatus) {
        this.payLogStatus = payLogStatus;
    }

    public Date getBeginCreateTime() {
        return beginCreateTime;
    }

    public void setBeginCreateTime(Date beginCreateTime) {
        this.beginCreateTime = beginCreateTime;
    }

    public Date getEndCreateTime() {
        return endCreateTime;
    }

    public void setEndCreateTime(Date endCreateTime) {
        this.endCreateTime = endCreateTime;
    }

    public Date getBeginUpdateTime() {
        return beginUpdateTime;
    }

    public void setBeginUpdateTime(Date beginUpdateTime) {
        this.beginUpdateTime = beginUpdateTime;
    }

    public Date getEndUpdateTime() {
        return endUpdateTime;
    }

    public void setEndUpdateTime(Date endUpdateTime) {
        this.endUpdateTime = endUpdateTime;
    }

}