package com.yilidi.o2o.order.model.result;

import com.yilidi.o2o.core.model.BaseModel;

/**
 * 
 * 销量汇总统计
 * 
 * @author: heyong
 * @date: 2015年11月27日 上午11:25:26
 * 
 */
public class BaseVolumeStatisticsInfo extends BaseModel {

	private static final long serialVersionUID = 3258178788334161692L;

	/**
	 * 下单数量
	 */
	private Integer totalOrderCount;

	/**
	 * 订单金额
	 */
	private Long totalAmount;

	/**
	 * 用户数
	 */
	private Integer totalUserCount;
	
	/**
	 * 下单的日期 不包括时分秒
	 */
	private String orderTime;

	public Integer getTotalOrderCount() {
		return totalOrderCount;
	}

	public void setTotalOrderCount(Integer totalOrderCount) {
		this.totalOrderCount = totalOrderCount;
	}

	public Long getTotalAmount() {
		return totalAmount;
	}

	public void setTotalAmount(Long totalAmount) {
		this.totalAmount = totalAmount;
	}

	public Integer getTotalUserCount() {
		return totalUserCount;
	}

	public void setTotalUserCount(Integer totalUserCount) {
		this.totalUserCount = totalUserCount;
	}

	public String getOrderTime() {
		return orderTime;
	}

	public void setOrderTime(String orderTime) {
		this.orderTime = orderTime;
	}

}
