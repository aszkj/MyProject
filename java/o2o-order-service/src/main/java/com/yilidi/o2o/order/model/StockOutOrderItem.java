package com.yilidi.o2o.order.model;

import com.yilidi.o2o.core.model.BaseModel;

/**
 * 出库单明细历史变化表，映射交易域表YiLiDiOrderCenter.T_STOCKOUT_ORDER_ITEM
 * 
 * @author chenb
 * 
 */
public class StockOutOrderItem extends BaseModel {

    private static final long serialVersionUID = -1528608633302249920L;
    /**
     * 出库单明细ID，主键自增
     */
    private Integer id;
    /**
     * 出库单编号
     */
    private String stockOutOrderNo;
    /**
     * 产品ID
     */
    private Integer productId;
    /**
     * 商品ID
     */
    private Integer saleProductId;
    /**
     * 出库时商品名称
     */
    private String productName;
    /**
     * 出库时商品条形码
     */
    private String barCode;
    /**
     * 出库时商品规格
     */
    private String specifications;
    /**
     * 出库时商品类别名称
     */
    private String productClassName;
    /**
     * 出库数量
     */
    private Integer quantity;
    /**
     * 出库商品的价格
     */
    private Long orderPrice;
    /**
     * 出库商品总价
     */
    private Long totalPrice;
    /**
     * 出库时商品列表图URL
     */
    private String productImageUrl3;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getStockOutOrderNo() {
        return stockOutOrderNo;
    }

    public void setStockOutOrderNo(String stockOutOrderNo) {
        this.stockOutOrderNo = stockOutOrderNo;
    }

    public Integer getProductId() {
        return productId;
    }

    public void setProductId(Integer productId) {
        this.productId = productId;
    }

    public Integer getSaleProductId() {
        return saleProductId;
    }

    public void setSaleProductId(Integer saleProductId) {
        this.saleProductId = saleProductId;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getBarCode() {
        return barCode;
    }

    public void setBarCode(String barCode) {
        this.barCode = barCode;
    }

    public String getSpecifications() {
        return specifications;
    }

    public void setSpecifications(String specifications) {
        this.specifications = specifications;
    }

    public String getProductClassName() {
        return productClassName;
    }

    public void setProductClassName(String productClassName) {
        this.productClassName = productClassName;
    }

    public Integer getQuantity() {
        return quantity;
    }

    public void setQuantity(Integer quantity) {
        this.quantity = quantity;
    }

    public Long getOrderPrice() {
        return orderPrice;
    }

    public void setOrderPrice(Long orderPrice) {
        this.orderPrice = orderPrice;
    }

    public Long getTotalPrice() {
        return totalPrice;
    }

    public void setTotalPrice(Long totalPrice) {
        this.totalPrice = totalPrice;
    }

    public String getProductImageUrl3() {
        return productImageUrl3;
    }

    public void setProductImageUrl3(String productImageUrl3) {
        this.productImageUrl3 = productImageUrl3;
    }

}
