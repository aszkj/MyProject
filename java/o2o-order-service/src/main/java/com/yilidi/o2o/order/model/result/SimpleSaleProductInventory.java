package com.yilidi.o2o.order.model.result;

import com.yilidi.o2o.core.model.BaseModel;

/**
 * 简单的库存信息
 * 
 * @author simpson
 * 
 */
public class SimpleSaleProductInventory extends BaseModel {

    private static final long serialVersionUID = 2292971577030786720L;
    /**
     * 供应商产品ID， 关联产品域供应商产品表P_SALE_PRODUCT的SALEPRODUCTID字段
     */
    private Integer saleProductId;
    /**
     * 库存量，当前的实际库存量
     */
    private Integer remainCount;

    public Integer getSaleProductId() {
        return saleProductId;
    }

    public void setSaleProductId(Integer saleProductId) {
        this.saleProductId = saleProductId;
    }

    public Integer getRemainCount() {
        return remainCount;
    }

    public void setRemainCount(Integer remainCount) {
        this.remainCount = remainCount;
    }

}
