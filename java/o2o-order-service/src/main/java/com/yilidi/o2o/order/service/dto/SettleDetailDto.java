package com.yilidi.o2o.order.service.dto;

import com.yilidi.o2o.core.model.BaseDto;

public class SettleDetailDto extends BaseDto {

    /**
     * @Fields serialVersionUID : TODO(serialVersionUID)
     */
    private static final long serialVersionUID = -7185579307802183692L;

    /**
     * 统计日期
     */
    private String statisticDate;
    /**
     * 订单编码
     */
    private String saleOrderNo;
    /**
     * 结算金额
     */
    private Long settleAmount;

    public String getStatisticDate() {
        return statisticDate;
    }

    public void setStatisticDate(String statisticDate) {
        this.statisticDate = statisticDate;
    }

    public String getSaleOrderNo() {
        return saleOrderNo;
    }

    public void setSaleOrderNo(String saleOrderNo) {
        this.saleOrderNo = saleOrderNo;
    }

    public Long getSettleAmount() {
        return settleAmount;
    }

    public void setSettleAmount(Long settleAmount) {
        this.settleAmount = settleAmount;
    }

}
