package com.yilidi.o2o.order.model.result;

import java.util.Date;

import com.yilidi.o2o.core.model.BaseModel;

/**
 * 推荐订单信息
 * 
 * @author: chenlian
 * @date: 2016年8月12日 上午1:34:14
 */
public class RecommendOrderInfo extends BaseModel {

    /**
     * serialVersionUID
     */
    private static final long serialVersionUID = -5008967139849979608L;

    /**
     * 推广人真实姓名
     */
    private String recommendRealName;

    /**
     * 推广人手机号
     */
    private String recommendMobile;

    /**
     * 邀请码
     */
    private String invitationCode;

    /**
     * 买家手机号
     */
    private String buyerMobile;

    /**
     * 买家注册时间
     */
    private Date buyerRegisterTime;

    /**
     * 订单编号
     */
    private String saleOrderNo;

    /**
     * 订单金额
     */
    private Long saleOrderAmount;

    /**
     * 订单状态
     */
    private String saleOrderStatus;

    public String getRecommendRealName() {
        return recommendRealName;
    }

    public void setRecommendRealName(String recommendRealName) {
        this.recommendRealName = recommendRealName;
    }

    public String getRecommendMobile() {
        return recommendMobile;
    }

    public void setRecommendMobile(String recommendMobile) {
        this.recommendMobile = recommendMobile;
    }

    public String getInvitationCode() {
        return invitationCode;
    }

    public void setInvitationCode(String invitationCode) {
        this.invitationCode = invitationCode;
    }

    public String getBuyerMobile() {
        return buyerMobile;
    }

    public void setBuyerMobile(String buyerMobile) {
        this.buyerMobile = buyerMobile;
    }

    public Date getBuyerRegisterTime() {
        return buyerRegisterTime;
    }

    public void setBuyerRegisterTime(Date buyerRegisterTime) {
        this.buyerRegisterTime = buyerRegisterTime;
    }

    public String getSaleOrderNo() {
        return saleOrderNo;
    }

    public void setSaleOrderNo(String saleOrderNo) {
        this.saleOrderNo = saleOrderNo;
    }

    public Long getSaleOrderAmount() {
        return saleOrderAmount;
    }

    public void setSaleOrderAmount(Long saleOrderAmount) {
        this.saleOrderAmount = saleOrderAmount;
    }

    public String getSaleOrderStatus() {
        return saleOrderStatus;
    }

    public void setSaleOrderStatus(String saleOrderStatus) {
        this.saleOrderStatus = saleOrderStatus;
    }

}
