package com.yilidi.o2o.order.service.dto;

import com.yilidi.o2o.core.model.BaseDto;

/**
 * 
 * 销量汇总统计
 * 
 * @author: heyong
 * @date: 2015年11月27日 上午11:25:26
 * 
 */
public class AllVolumeStatisticsInfoDto extends BaseDto {

	private static final long serialVersionUID = 7808092827497523149L;

	/**
	 * 下单的门店名称
	 */
	private String storeName;

	/**
	 * 下单的门店编号
	 */
	private String storeCode;

	/**
	 * 总下单数量
	 */
	private Integer totalOrderCount;

	/**
	 * 总订单金额
	 */
	private Long totalAmount;

	/**
	 * 成交下单数量
	 */
	private Integer totalOrderCountFromSuccess;

	/**
	 * 成交的总订单金额
	 */
	private Long totalAmountFromSuccess;

	/**
	 * 成交的用户数
	 */
	private Integer totalUserCountFromSuccess;

	/**
	 * 取消的下单数量
	 */
	private Integer totalOrderCountFromCancel;

	/**
	 * 取消的订单金额
	 */
	private Long totalAmountFromCancel;

	/**
	 * 订单使用的在线支付的补贴金额（现金补贴金额）
	 */
	private Long cashSubsidy;
	/**
	 * 订单使用的优惠券/代金券的补贴金额
	 */
	private Long couponSubsidy;
	/**
	 * 订单中商品差价补贴的总金额
	 */
	private Long priceSubsidy;
	/**
	 * 订单物流补贴金额，根据系统设置订单满多少给予物流补贴
	 */
	private Long logisticsSubsidy;

	/**
	 * 下单的日期 不包括时分秒
	 */
	private String orderTime;

	public String getStoreName() {
		return storeName;
	}

	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}

	public String getStoreCode() {
		return storeCode;
	}

	public void setStoreCode(String storeCode) {
		this.storeCode = storeCode;
	}

	public Integer getTotalOrderCount() {
		return totalOrderCount;
	}

	public void setTotalOrderCount(Integer totalOrderCount) {
		this.totalOrderCount = totalOrderCount;
	}

	public Long getTotalAmount() {
		return totalAmount;
	}

	public void setTotalAmount(Long totalAmount) {
		this.totalAmount = totalAmount;
	}

	public Integer getTotalOrderCountFromSuccess() {
		return totalOrderCountFromSuccess;
	}

	public void setTotalOrderCountFromSuccess(Integer totalOrderCountFromSuccess) {
		this.totalOrderCountFromSuccess = totalOrderCountFromSuccess;
	}

	public Long getTotalAmountFromSuccess() {
		return totalAmountFromSuccess;
	}

	public void setTotalAmountFromSuccess(Long totalAmountFromSuccess) {
		this.totalAmountFromSuccess = totalAmountFromSuccess;
	}

	public Integer getTotalUserCountFromSuccess() {
		return totalUserCountFromSuccess;
	}

	public void setTotalUserCountFromSuccess(Integer totalUserCountFromSuccess) {
		this.totalUserCountFromSuccess = totalUserCountFromSuccess;
	}

	public Integer getTotalOrderCountFromCancel() {
		return totalOrderCountFromCancel;
	}

	public void setTotalOrderCountFromCancel(Integer totalOrderCountFromCancel) {
		this.totalOrderCountFromCancel = totalOrderCountFromCancel;
	}

	public Long getTotalAmountFromCancel() {
		return totalAmountFromCancel;
	}

	public void setTotalAmountFromCancel(Long totalAmountFromCancel) {
		this.totalAmountFromCancel = totalAmountFromCancel;
	}

	public Long getCashSubsidy() {
		return cashSubsidy;
	}

	public void setCashSubsidy(Long cashSubsidy) {
		this.cashSubsidy = cashSubsidy;
	}

	public Long getCouponSubsidy() {
		return couponSubsidy;
	}

	public void setCouponSubsidy(Long couponSubsidy) {
		this.couponSubsidy = couponSubsidy;
	}

	public Long getPriceSubsidy() {
		return priceSubsidy;
	}

	public void setPriceSubsidy(Long priceSubsidy) {
		this.priceSubsidy = priceSubsidy;
	}

	public Long getLogisticsSubsidy() {
		return logisticsSubsidy;
	}

	public void setLogisticsSubsidy(Long logisticsSubsidy) {
		this.logisticsSubsidy = logisticsSubsidy;
	}

	public String getOrderTime() {
		return orderTime;
	}

	public void setOrderTime(String orderTime) {
		this.orderTime = orderTime;
	}

}
