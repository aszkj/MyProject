package com.yilidi.o2o.order.service.dto;

import com.yilidi.o2o.core.model.BaseDto;

/**
 * 创建出库单前台传过来的参数DTO
 * 
 * @author chenb
 * 
 */
public class StockOutOrderCreateParamDto extends BaseDto {

    private static final long serialVersionUID = 3334825049075654138L;
    /**
     * 商户ID
     */
    private String storeId;
    /**
     * 产品明细组合字符串
     */
    private String productItems;
    /**
     * 出库单类型
     */
    private String stockOutOrderType;

    public String getStoreId() {
        return storeId;
    }

    public void setStoreId(String storeId) {
        this.storeId = storeId;
    }

    public String getProductItems() {
        return productItems;
    }

    public void setProductItems(String productItems) {
        this.productItems = productItems;
    }

    public String getStockOutOrderType() {
        return stockOutOrderType;
    }

    public void setStockOutOrderType(String stockOutOrderType) {
        this.stockOutOrderType = stockOutOrderType;
    }

}
