package com.yilidi.o2o.order.model;

import com.yilidi.o2o.core.model.BaseModel;

/**
 * 采购单收货地址信息，映射交易域表YiLiDiOrderCenter.T_PURCHASE_ORDER_ADDRESS
 * 
 * @author chenb
 * 
 */
public class PurchaseOrderAddress extends BaseModel {

    private static final long serialVersionUID = 1625216950927281637L;
    /**
     * 采购收货地址ID，主键自增
     */
    private Integer id;
    /**
     * 采购单编号
     */
    private String purchaseOrderNo;
    /**
     * 收货人名称
     */
    private String userName;
    /**
     * 收货地址
     */
    private String addressDetail;
    /**
     * 手机号码
     */
    private String phoneNo;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getPurchaseOrderNo() {
        return purchaseOrderNo;
    }

    public void setPurchaseOrderNo(String purchaseOrderNo) {
        this.purchaseOrderNo = purchaseOrderNo;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getAddressDetail() {
        return addressDetail;
    }

    public void setAddressDetail(String addressDetail) {
        this.addressDetail = addressDetail;
    }

    public String getPhoneNo() {
        return phoneNo;
    }

    public void setPhoneNo(String phoneNo) {
        this.phoneNo = phoneNo;
    }

}
