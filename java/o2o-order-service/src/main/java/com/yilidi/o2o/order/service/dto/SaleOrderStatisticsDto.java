package com.yilidi.o2o.order.service.dto;

import com.yilidi.o2o.core.model.BaseDto;

/**
 * 销售统计 ->用户销售统计Dto
 * 
 * @author: heyong
 * @date: 2015年11月25日 下午4:55:40
 * 
 */
public class SaleOrderStatisticsDto extends BaseDto {

	private static final long serialVersionUID = 1387521508076518884L;

	/**
	 * 下单的用户ID
	 */
	private Integer userId;

	/**
	 * 下单的用户账号
	 */
	private String userName;

	/**
	 * 总下单数量
	 */
	private Integer totalOrderCount;

	/**
	 * 总订单金额
	 */
	private Long totalAmount;

	/**
	 * 成交下单数量
	 */
	private Integer totalOrderCountFromSuccess;

	/**
	 * 成交的总订单金额
	 */
	private Long totalAmountFromSuccess;

	/**
	 * 取消的下单数量
	 */
	private Integer totalOrderCountFromCancel;

	/**
	 * 取消的订单金额
	 */
	private Long totalAmountFromCancel;

	/**
	 * 下单的日期 不包括时分秒
	 */
	private String orderTime;

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public Integer getTotalOrderCount() {
		return totalOrderCount;
	}

	public void setTotalOrderCount(Integer totalOrderCount) {
		this.totalOrderCount = totalOrderCount;
	}

	public Long getTotalAmount() {
		return totalAmount;
	}

	public void setTotalAmount(Long totalAmount) {
		this.totalAmount = totalAmount;
	}

	public Integer getTotalOrderCountFromSuccess() {
		return totalOrderCountFromSuccess;
	}

	public void setTotalOrderCountFromSuccess(Integer totalOrderCountFromSuccess) {
		this.totalOrderCountFromSuccess = totalOrderCountFromSuccess;
	}

	public Long getTotalAmountFromSuccess() {
		return totalAmountFromSuccess;
	}

	public void setTotalAmountFromSuccess(Long totalAmountFromSuccess) {
		this.totalAmountFromSuccess = totalAmountFromSuccess;
	}

	public Integer getTotalOrderCountFromCancel() {
		return totalOrderCountFromCancel;
	}

	public void setTotalOrderCountFromCancel(Integer totalOrderCountFromCancel) {
		this.totalOrderCountFromCancel = totalOrderCountFromCancel;
	}

	public Long getTotalAmountFromCancel() {
		return totalAmountFromCancel;
	}

	public void setTotalAmountFromCancel(Long totalAmountFromCancel) {
		this.totalAmountFromCancel = totalAmountFromCancel;
	}

	public String getOrderTime() {
		return orderTime;
	}

	public void setOrderTime(String orderTime) {
		this.orderTime = orderTime;
	}
}
