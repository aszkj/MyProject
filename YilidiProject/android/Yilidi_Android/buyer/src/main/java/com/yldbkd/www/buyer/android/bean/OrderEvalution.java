package com.yldbkd.www.buyer.android.bean;

import java.util.List;

/**
 * 订单评价
 * Created by lizhg on 17/02/08.
 */
public class OrderEvalution extends BaseModel {
    /**
     * 店铺ID
     */
    private Integer storeId;
    /**
     * 服务评分（最高5.0分）
     */
    private Float serviceScore;
    /**
     * 配送评分（最高5.0分）
     */
    private Float deliverScore;
    /**
     * 是否为匿名评价标识：
     * 0：不匿名（默认）
     * 1：匿名评价
     */
    private Integer isAnonymous;
    /**
     * 商品评价信息列表
     */
    private List<Evalution> saleProductEvaluations;

    public Integer getStoreId() {
        return storeId;
    }

    public void setStoreId(Integer storeId) {
        this.storeId = storeId;
    }

    public Float getServiceScore() {
        return serviceScore;
    }

    public void setServiceScore(Float serviceScore) {
        this.serviceScore = serviceScore;
    }

    public Float getDeliverScore() {
        return deliverScore;
    }

    public void setDeliverScore(Float deliverScore) {
        this.deliverScore = deliverScore;
    }

    public Integer getIsAnonymous() {
        return isAnonymous;
    }

    public void setIsAnonymous(Integer isAnonymous) {
        this.isAnonymous = isAnonymous;
    }

    public List<Evalution> getSaleProductEvaluations() {
        return saleProductEvaluations;
    }

    public void setSaleProductEvaluations(List<Evalution> saleProductEvaluations) {
        this.saleProductEvaluations = saleProductEvaluations;
    }
}
