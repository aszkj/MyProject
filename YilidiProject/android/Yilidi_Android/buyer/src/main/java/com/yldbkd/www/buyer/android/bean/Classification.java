package com.yldbkd.www.buyer.android.bean;

import java.util.List;

/**
 * 分类数据类
 * <p/>
 * Created by linghuxj on 15/9/29.
 */
public class Classification extends BaseModel {

    /**
     * 类型Code
     */
    private String classCode;
    /**
     * 类型名称
     */
    private String className;
    /**
     * 类型图片
     */
    private String classImageUrl;
    /**
     * 类型图片
     */
    private List<Classification> subClassList;

    public String getClassCode() {
        return classCode;
    }

    public void setClassCode(String classCode) {
        this.classCode = classCode;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getClassImageUrl() {
        return classImageUrl;
    }

    public void setClassImageUrl(String classImageUrl) {
        this.classImageUrl = classImageUrl;
    }

    public List<Classification> getSubClassList() {
        return subClassList;
    }

    public void setSubClassList(List<Classification> subClassList) {
        this.subClassList = subClassList;
    }
}
