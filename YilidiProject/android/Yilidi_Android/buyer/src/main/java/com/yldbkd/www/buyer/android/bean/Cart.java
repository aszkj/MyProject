package com.yldbkd.www.buyer.android.bean;

import java.util.List;

/**
 * 购物车数据类型
 * <p/>
 * Created by linghuxj on 15/10/20.
 */
public class Cart extends BaseModel {

    /**
     * 购物车列表(根据店铺拆分)
     */
    private List<CartStore> shopCartList;
    /**
     * 系统当前时间
     */
    private String currentTime;
    /**
     * 系统默认地址
     */
    private AddressBase consigneeAddress;

    public List<CartStore> getShopCartList() {
        return shopCartList;
    }

    public void setShopCartList(List<CartStore> shopCartList) {
        this.shopCartList = shopCartList;
    }

    public String getCurrentTime() {
        return currentTime;
    }

    public void setCurrentTime(String currentTime) {
        this.currentTime = currentTime;
    }

    public AddressBase getConsigneeAddress() {
        return consigneeAddress;
    }

    public void setConsigneeAddress(AddressBase consigneeAddress) {
        this.consigneeAddress = consigneeAddress;
    }
}
