package com.yldbkd.www.seller.android.bean;

import java.util.List;

/**
 * 调货单详细信息数据
 * <p/>
 * Created by linghuxj on 16/5/28.
 */
public class AllotOrderDetail extends AllotOrderBase {

    /**
     * 备注
     */
    private String note;
    /**
     * 调入仓库
     */
    private String allotToStoreName;
    /**
     * 调拨单状态信息列表
     */
    private List<AllotStatus> allotStatusList;
    /**
     * 调拨商品列表
     */
    private List<ProductAllot> allotOrderItemList;

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public String getAllotToStoreName() {
        return allotToStoreName;
    }

    public void setAllotToStoreName(String allotToStoreName) {
        this.allotToStoreName = allotToStoreName;
    }

    public List<AllotStatus> getAllotStatusList() {
        return allotStatusList;
    }

    public void setAllotStatusList(List<AllotStatus> allotStatusList) {
        this.allotStatusList = allotStatusList;
    }

    public List<ProductAllot> getAllotOrderItemList() {
        return allotOrderItemList;
    }

    public void setAllotOrderItemList(List<ProductAllot> allotOrderItemList) {
        this.allotOrderItemList = allotOrderItemList;
    }
}
