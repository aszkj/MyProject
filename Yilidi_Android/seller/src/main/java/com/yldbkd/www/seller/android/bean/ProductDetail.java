package com.yldbkd.www.seller.android.bean;

/**
 * 商品详细信息数据
 * <p/>
 * Created by linghuxj on 16/5/28.
 */
public class ProductDetail extends ProductBase {

    /**
     * 商品普通价
     */
    private Long retailPrice;
    /**
     * 商品会员价
     */
    private Long promotionalPrice;
    /**
     * 商品销售数量
     */
    private Integer saleCount;
    /**
     * 商品剩余库存数量
     */
    private Integer remainCount;

    private boolean isCheck = false;

    public Long getRetailPrice() {
        return retailPrice;
    }

    public void setRetailPrice(Long retailPrice) {
        this.retailPrice = retailPrice;
    }

    public Long getPromotionalPrice() {
        return promotionalPrice;
    }

    public void setPromotionalPrice(Long promotionalPrice) {
        this.promotionalPrice = promotionalPrice;
    }

    public Integer getSaleCount() {
        return saleCount;
    }

    public void setSaleCount(Integer saleCount) {
        this.saleCount = saleCount;
    }

    public Integer getRemainCount() {
        return remainCount;
    }

    public void setRemainCount(Integer remainCount) {
        this.remainCount = remainCount;
    }

    public boolean isCheck() {
        return isCheck;
    }

    public void setCheck(boolean check) {
        isCheck = check;
    }
}
