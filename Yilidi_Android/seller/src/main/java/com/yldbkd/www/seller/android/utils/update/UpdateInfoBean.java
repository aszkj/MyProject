package com.yldbkd.www.seller.android.utils.update;

import java.io.Serializable;

/**
 * 版本
 *
 * @author 尤振华
 */
public class UpdateInfoBean implements Serializable {

    /**
     *
     */
    private static final long serialVersionUID = 4130420942151069813L;
    private String versionName;
    private int versionCode;
    private boolean force;
    private String url;
    private String desc;
    private String size;
    private String key;

    public UpdateInfoBean() {
    }

    public UpdateInfoBean(boolean isnew, String versionName, int versionCode, boolean force, String url) {

        this.versionName = versionName;
        this.versionCode = versionCode;
        this.force = force;
        this.url = url;
    }

    public String getVersionName() {
        return versionName;
    }

    public void setVersionName(String versionName) {
        this.versionName = versionName;
    }

    public int getVersionCode() {
        return versionCode;
    }

    public void setVersionCode(int versionCode) {
        this.versionCode = versionCode;
    }

    public boolean isForce() {
        return force;
    }

    public void setForce(boolean force) {
        this.force = force;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
